class Persona{

  String name;
  String lastName;
  int age;

  Persona(){
    this.name = null;
    this.lastName = null;
    this.age = 0;
  }

  void set Name(String name){
    name = name;
  }

  String get Name{
    return name;
  }

  void set LastName(String lastName){
    lastName = lastName;
  }

  String get LastName{
    return lastName;
}

  void set Age(int age){
    age = age;
  }

  int get Age{
    return age;
  }

}